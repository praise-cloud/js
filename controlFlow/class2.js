//switch statements

let age = 19;
switch(age){
    case 18:
        console.log("you are 18 years old");
        break;
    case 19:
        console.log("you are 19 years old");
        break;
    case 20:
        console.log("you are 20 years old");
        break;
    default:
        console.log("you are not 18 or 19 or 20 years old");
}



